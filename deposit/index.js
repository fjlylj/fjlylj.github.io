function formatAddress(address) {
    if (address == "" || address == null || address.trim == "") return "";
    return address.slice(0, 4) + "..." + address.slice(-4);
}

let address = "0x08B1256E062F97efffE7e544f486fC0F8C19FC5f";
let provider,
    signer,
    DepositEth;

(async () => {
    if (window.ethereum) {

        // Request Metamask for accounts
        await window.ethereum.request({ method: "eth_requestAccounts" });
    
        // SetProvider
        provider = new ethers.providers.Web3Provider(window.ethereum);;
    
        // Set Current Signer
        signer = provider.getSigner();
        let account = await signer.getAddress();
        DepositEth = new ABIDecoder(provider, abi, "DepositEth").decode(address);


        $("#account").text("当前账户："+ formatAddress(account));

        let balanceOf = await DepositEth.methods.balanceOf(account);
        
        let html = ""
        for (let i = 0; i < balanceOf.length; i++) {
            let balance = balanceOf[i] / 1;
            let info = await DepositEth.methods._deposits(balance);
            
            html += ' <tr>'+
            '<td>' + info.id / 1 + '</td>'+
            '<td>' + new Date((info.timestamp / 1) * 1000).toLocaleDateString() + '</td>'+
            '<td>' + new Date((info.unlockTimestamp / 1) * 1000).toLocaleDateString() + '</td>'+
            '<td>' + ethers.utils.formatEther(info.value) + '</td>'+
            '<td><button onclick="redeem(' + info.id / 1 +')">赎回</button></td>'+
        '</tr>'
        }

        $("#deposit_info").html(html);
    } else {
        alert("请安装MetaMask钱包")
    }


    
})()

async function deposit() {
    let time = $("#depositTime").val();
    let amount = $("#value").val();
    window.alert("等待交易打包")
    await DepositEth.methods.deposit(new Date(time).getTime() / 1000, {
        value: ethers.utils.parseEther(amount)
    }).then(() => {
        window.location.reload()
    }).catch(() => {
        alert("fail");
    });
}


async function redeem(depositId) {
    await DepositEth.methods.redeem(depositId).then(() => {
        window.location.reload()
    }).catch(() => {
        alert("fail")
    });
}